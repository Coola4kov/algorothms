# 101 & 110 = 100 (4)
print("Побитовое умножение 5 и 6 = {}".format(5 & 6))
# 101 | 110 = 111 (7)
print("Побитовое сложение 5 и 6 = {}".format(5 | 6))
# 101 ^ 110 = 011 (3)
print("Побитовое исключающее или 5 и 6 = {}".format(5 ^ 6))
# 101 << (2 позиции) = 10100  (20)
print("Побитовый сдвиг влево = {}".format(5 << 2))
# 101 >> (2 позиции) = 1  (1)
print("Побитовый сдвиг вправо = {}".format(5 >> 2))
# ~101 = 111111...111010 (-6)
print("Инверсия битов = {}".format(~5))


